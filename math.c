// the mathematical functions in c using library #include<math.h>
#include<stdio.h>
#include<math.h>

int main()
{

  printf("%f\n", pow(2,3) ); // we have to use %f not %d, otherwise it will show 0 as answer.
  printf("%f\n", sqrt(76) );
  printf("%f\n", ceil(53.2) ); // next highest value.
  printf("%f\n", floor(-78.867) ); // GIF

  return 0;
}